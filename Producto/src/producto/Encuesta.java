    private void btnResActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResActionPerformed
        // TODO add your handling code here:
        DecimalFormat df = new DecimalFormat("###");
        DecimalFormat df1 = new DecimalFormat("###.##");
        double porcSi = (contSi/totalEnc)*100;
        lblTotal.setText(String.valueOf(df.format(totalEnc)));
        lblNo.setText(String.valueOf(df.format(contNo)));
        lblSi.setText(String.valueOf(df.format(contSi)));
        lblPos.setText(String.valueOf(df.format(contPos)));
        lblCoca.setText(String.valueOf(df.format(contCoca)));
        lblPool.setText(String.valueOf(df.format(contPool)));
        lblOtra.setText(String.valueOf(df.format(contOtra)));
        lblPorc.setText(String.valueOf(df1.format(porcSi)) + "%");
        
        
    }//GEN-LAST:event_btnResActionPerformed

